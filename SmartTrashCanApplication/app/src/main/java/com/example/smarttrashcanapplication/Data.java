package com.example.smarttrashcanapplication;


public class Data {
    String fillData;
    String airData;
    String time;

    public Data() {};

    public String getFillData() {
        return fillData;
    }

    public void setFillData(String fillData) {
        this.fillData = fillData;
    }

    public String getAirData() {
        return airData;
    }

    public void setAirData(String airData) {
        this.airData = airData;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}